package ejemplos_InOut.Ejercicios.Repaso_Algoritmia;

public class Alumno {
    private int nombre;
    private float[] notas;

    public Alumno(int nombre, float[] notas) {
        this.nombre = nombre;
        this.notas = notas;
    }

    public int getNombre() {
        return nombre;
    }

    public float[] getNotas() {
        return notas;
    }

 
}
